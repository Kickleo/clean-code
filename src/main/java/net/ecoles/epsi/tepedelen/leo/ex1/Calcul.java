package net.ecoles.epsi.tepedelen.leo.ex1;


/**
 * Classe qui implémente l'addition et la soustraction
 */
public class Calcul {

    /**
     * Additionne a à b
     */
    public int addition(int a, int b) {
        return a + b;
    }

    /**
     * Soustrait b à a
     */
    public int soustraction(int a, int b) {
        return a - b;
    }
}
